/****
 * Formats a number according to the language convention
 * Parameters
 *   - f: the number to format
 *   - $decimals: number of digits
 * Created: 03/2009, following tip at
 *    http://fredericiana.com/2008/05/29/php-multi-byte-safe-number_format-function/#more-1287
 */
function format_float($f, $decimals=0) {
	$phpTANumberFormat = array(".",","); // Decimal separator, thousands separator
	$placeholders = array('@', '~');
	// format number with placeholders
	$formatted = number_format($f, $decimals, $placeholders[0], $placeholders[1]);
	// replace by localized characters
	$formatted = str_replace($placeholders, $phpTANumberFormat, $formatted);
	return $formatted;
}

/****
 * Returns a string such as 1 hit day if singular or 
 * 15 hits for plural.
 * Can be adapted for languages with complicated purals, such as Polish
 * Parameters
 *   - n: the number to format
 * Created: 03/2009
 */
function hits($n) {
	$num = format_float($n, 0);
	if ($n > 1) return "$num hits";
	return "$num hit";
}

