/************************************************************************
 Returns a date with day, month, and year
 - phrase=0: just the date
 - phrase=1: in a phrase (not important in English, but important for other languages)
 - phrase=1: at the begining of a phrase (not important in English, but important for other languages)
*************************************************************************/
function dayMmYear ($time, $phrase = 0) {
	$monthArr = array ("", "January", "February", "March", "April", "May", "June", "July", "August", "September", "October", "November", "December");
	$date = $monthArr[date("n", $time)]." ".date("d, Y", $time);
	return $date;
}

/************************************************************************
 Returns a date with day, month, and year, and time
 - phrase=0: just the date
 - phrase=1: in a phrase (not important in English, but important for other languages)
 - phrase=1: at the begining of a phrase (not important in English, but important for other languages)
*************************************************************************/
function dateandtime ($time, $phrase = 0) {
	$monthArr = array ("", "January", "February", "March", "April", "May", "June", "July", "August", "September", "October", "November", "December");
	$date = $monthArr[date("n", $time)]." ".date("d, Y, H:i", $time);
	return $date;
}

/************************************************************************
 Returns month and year
*************************************************************************/
function monthYear ($time) {
	$monthArr = array ("", "January", "February", "March", "April", "May", "June", "July", "August", "September", "October", "November", "December");
	$date = $monthArr[date("n", $time)].date(" Y", $time);
	return $date;
}

/************************************************************************
 Returns month and year as Jan-06 for instance
*************************************************************************/
function shortMonthYear($time) {
	global $strings;
	$monthArr = array("", $strings['jan'], $strings['feb'], $strings['mar'], $strings['apr'], $strings['ma'], $strings['jun'], $strings['jul'], $strings['aug'], $strings['sep'], $strings['oct'], $strings['nov'], $strings['dec']);
	return $monthArr[date("n", $time)]." ".date("y", $time);
}

/************************************************************************
 Returns a time
 - phrase=0: just the date
 - phrase=1: in a phrase
 - phrase=1: at the begining of a phrase
*************************************************************************/
function timetoday ($time, $phrase = 0) {
	$date = date("H:i", $time);
	if ($phrase == 1) {
		$date = "today at $date";
	} else if ($phrase == 2) {
		$date = "Today at $date";
	}
	return $date;
}

